    private void srediFormu() {
        try {
            Ispit i = new Ispit();
            TObjekat posalji = new TObjekat(i, "vratiSveIspite");
            Komunikacija.vratiObjekat().posalji(posalji);
            List<Ispit> listaIspita = (List<Ispit>) Komunikacija.vratiObjekat().procitaj().getObjekat();
            SviIspitiTableModel itm = new SviIspitiTableModel(listaIspita);
            jtblIspiti.setModel(itm);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
