    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            SviIspitiTableModel itm = (SviIspitiTableModel) jtblIspiti.getModel();
            Ispit i = itm.getListaIspita().get(jtblIspiti.getSelectedRow());
            Student s = (Student) Komunikacija.vratiObjekat().getHashMapa().get("student");
            Sluzbenik sl = (Sluzbenik) Komunikacija.vratiObjekat().getHashMapa().get("sluzbenik");
            PolozenIspit ispit = new PolozenIspit(i, s, 0, true, sl);
            TObjekat posalji = new TObjekat(ispit, "prijavi");
            Komunikacija.vratiObjekat().posalji(posalji);
            TObjekat odgovor = Komunikacija.vratiObjekat().procitaj();
            if (odgovor.getPoruka().equals("ok")){
                JOptionPane.showMessageDialog(this, "Ispit je prijavljen");
            }else{
                JOptionPane.showMessageDialog(this, "Ispit nije prijavljen");
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

