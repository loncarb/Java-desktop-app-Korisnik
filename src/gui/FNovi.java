    private void srediFormu() {
        try {
            comboMesto.removeAllItems();
            Mesto m = new Mesto();
            TObjekat posalji = new TObjekat(m, "vratiMesta");
            Komunikacija.vratiObjekat().posalji(posalji);

            List<Mesto> lista = (List<Mesto>) Komunikacija.vratiObjekat().procitaj().getObjekat();

            for (Mesto mesto : lista) {
                comboMesto.addItem(mesto);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
