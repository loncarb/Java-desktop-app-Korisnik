    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed
        try {
            String ime = txtIme.getText().trim();
            String prezime = txtPrezime.getText().trim();
            String JMBGtext = txtJMBG.getText().trim();
            int JMBG = Integer.parseInt(JMBGtext);
            String brIndeksa = txtBrIndeksa.getText().trim();
            String ulica = txtUlica.getText().trim();
            String broj = txtBroj.getText().trim();
            Mesto mesto = (Mesto) comboMesto.getSelectedItem();
            if (validacija()) {
                Student s = new Student(ime, prezime, brIndeksa, JMBG, new Adresa(ulica, broj, mesto));
                TObjekat posalji = new TObjekat(s1, "studentIzmeni");
                Komunikacija.vratiObjekat().posalji(posalji);
                TObjekat posalji1 = new TObjekat(s, null);
                Komunikacija.vratiObjekat().posalji(posalji1);
                TObjekat odgovor = Komunikacija.vratiObjekat().procitaj();
                if (odgovor.getPoruka().equals("ok")) {
                    JOptionPane.showMessageDialog(this, "Izmene su sacuvane");
                    StudentTableModel stm = (StudentTableModel) tabela.getModel();
                    Student stud = new Student();
                    TObjekat posalji6 = new TObjekat(stud, "vratiSveStudente");
                    Komunikacija.vratiObjekat().posalji(posalji6);
                    List<Student> listaStudenata = (List<Student>) Komunikacija.vratiObjekat().procitaj().getObjekat();
                    stm.setListaStudenata(listaStudenata);
                    stm.fireTableDataChanged();
                } else {
                    JOptionPane.showMessageDialog(this, "Doslo je do greske, izmene nisu sacuvane");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Unesite sve podatke");
            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jbtnIzmeniActionPerformed

