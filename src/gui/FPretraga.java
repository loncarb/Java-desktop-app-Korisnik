    private void srediFormu() {
        try {
            Student s = new Student();
            TObjekat posalji = new TObjekat(s, "vratiSveStudente");
            Komunikacija.vratiObjekat().posalji(posalji);

            List<Student> listaStudenata = (List<Student>) Komunikacija.vratiObjekat().procitaj().getObjekat();
            StudentTableModel mtm = new StudentTableModel(listaStudenata);
            jtblTabela.setModel(mtm);

            comboMesto.removeAllItems();

            Mesto m = new Mesto();
            TObjekat posalji2 = new TObjekat(m, "vratiMesta");
            Komunikacija.vratiObjekat().posalji(posalji2);

            List<Mesto> lista = (List<Mesto>) Komunikacija.vratiObjekat().procitaj().getObjekat();

            for (Mesto mesto : lista) {
                comboMesto.addItem(mesto);
            }
            Mesto nepoznato = null;
            comboMesto.addItem(nepoznato);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
