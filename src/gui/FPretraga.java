    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            int jmbg;
            if(txtJMBG.getText().equals("")){
                jmbg =0;
            }else{
                jmbg = Integer.parseInt(txtJMBG.getText());
            }
            Mesto m = (Mesto) comboMesto.getSelectedItem();
            Student s = new Student();
            s.setIme(ime);
            s.setJMBG(jmbg);
            s.setPrezime(prezime);
            Adresa a = new Adresa();
            a.setMesto(m);
            s.setAdresa(a);
            TObjekat posalji = new TObjekat(s, "nadji");
            Komunikacija.vratiObjekat().posalji(posalji);
            TObjekat odgovor = Komunikacija.vratiObjekat().procitaj();
            List<Student> lista = (List<Student>) odgovor.getObjekat();
            if (lista.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sistem ne može da nađe studenta prema zadatim vrednostima.");
            }
            StudentTableModel stm = new StudentTableModel(lista);
            jtblTabela.setModel(stm);
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Doslo je do greske, proverite podatke i pokusajte ponovo");
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

